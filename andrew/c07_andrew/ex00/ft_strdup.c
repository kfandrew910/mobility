#include <stdlib.h>

int	ft_strlen(char *str)
{
	int	length;

	length = 0;
	while (str[length] != '\0')
		length++;
	return (length);
}

char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

char *ft_strdup(char *src)
{
	char	*ptr;
	int 	len;

	len = ft_strlen(src);
	ptr = (char *)malloc(len * sizeof(char));
	ptr = ft_strcpy(ptr, src);
	return (ptr);
}
